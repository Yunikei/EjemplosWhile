namespace EjemplosWhile
{
    internal class Program
    {
        static void Main(string[] args)
        {
            int repetir = 100;
            int i = 0; //este es un contador

            while (i < repetir)
            {
                i++;
                Console.WriteLine("Bienvenido  i =" + i);
            }
            
            
            
        }
    }
}
